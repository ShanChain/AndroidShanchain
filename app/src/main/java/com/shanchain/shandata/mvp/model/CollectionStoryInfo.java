package com.shanchain.shandata.mvp.model;

public class CollectionStoryInfo {
    private String imgUrl;
    private String storyName;
    private String storyDes;
    private int storyResult;
    private String update;

    public String getImgUrl() {
        return imgUrl;
    }

    public void setImgUrl(String imgUrl) {
        this.imgUrl = imgUrl;
    }

    public String getStoryName() {
        return storyName;
    }

    public void setStoryName(String storyName) {
        this.storyName = storyName;
    }

    public String getStoryDes() {
        return storyDes;
    }

    public void setStoryDes(String storyDes) {
        this.storyDes = storyDes;
    }

    public int getStoryResult() {
        return storyResult;
    }

    public void setStoryResult(int storyResult) {
        this.storyResult = storyResult;
    }

    public String getUpdate() {
        return update;
    }

    public void setUpdate(String update) {
        this.update = update;
    }
}
